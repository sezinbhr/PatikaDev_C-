using System;

namespace Inheritance
{
    public class Creatures
    {
        protected void Food()
        {
            Console.WriteLine("The living creatures need food");

        }
        protected void Breathe()
        {
            Console.WriteLine("The living creatures breathe");

        }
        protected void Excretory()
        {
            Console.WriteLine("The living creatures do excretory");

        }

    }
}